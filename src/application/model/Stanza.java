package application.model;

public class Stanza {
	
	private int codiceStanza, numeroLetti, refCodiceReparto;

	public int getCodiceStanza() {
		return codiceStanza;
	}

	public void setCodiceStanza(int codiceStanza) {
		this.codiceStanza = codiceStanza;
	}

	public int getNumeroLetti() {
		return numeroLetti;
	}

	public void setNumeroLetti(int numeroLetti) {
		this.numeroLetti = numeroLetti;
	}

	public int getRefCodiceReparto() {
		return refCodiceReparto;
	}

	public void setRefCodiceReparto(int refCodiceReparto) {
		this.refCodiceReparto = refCodiceReparto;
	}
	
	

}
